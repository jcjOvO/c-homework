/*按照规律生成并输出数列前20项的值：第一项和第二项为2、3，
以后每个奇数编号项为前两项之和，偶数编号项为前两项之差（前项减后项）。*/

#include <stdio.h>

int main()
{
    int n;
    int a[20];

    a[0] = 2;
    a[1] = 3;
    for (n = 2; n < 20; n++)
    {
        a[n] = n % 2 == 1 ? a[n - 2] - a[n - 1] : a[n - 2] + a[n - 1];
    }
    for (n = 0; n < 20; n++)
    {
        printf("%d ", a[n]);
    }
    return 0;
}